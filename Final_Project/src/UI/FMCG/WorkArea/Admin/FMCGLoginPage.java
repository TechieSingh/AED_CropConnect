    private void btnLoginLoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginLoginButtonActionPerformed
        String un = txtUserNameField.getText();
        String pw = String.valueOf(txtPasswordField.getPassword());
        UserAccount useraccount = userAccountDirectory.AuthenticateUser(un, pw);
        if (useraccount == null) {
            FmcgUserAccount authenticatedUser = null;
            for (FMCG fmcg : fmcgDirectory.getFmcgs()) {
                authenticatedUser = fmcg.getFmcgUserAccountDirectory().AuthenticateUser(un, pw);
                if (authenticatedUser != null) {
                    break;
                }

            }
            if (authenticatedUser != null) {
                if (authenticatedUser.getAssociatedPersonProfile() instanceof RequirementOfficer requirementOfficer) {
                    ManageCropsRequirements manageCropsRequirements = new ManageCropsRequirements(mainWorkArea, requirementOfficer, marketDirectory);
                    mainWorkArea.add("ManageCropsRequirements", manageCropsRequirements);
                    CardLayout layout = (CardLayout) mainWorkArea.getLayout();
                    layout.next(mainWorkArea);
                }
                if (authenticatedUser.getAssociatedPersonProfile() instanceof ProcurementOfficer procurementOfficer) {
                    ManageCropsProcurement manageCropsProcurement = new ManageCropsProcurement(mainWorkArea, procurementOfficer,marketDirectory );
                    mainWorkArea.add("ManageCropsRequirements", manageCropsProcurement);
                    CardLayout layout = (CardLayout) mainWorkArea.getLayout();
                    layout.next(mainWorkArea);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid Credentials", "Authentication Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            Profile profile = useraccount.getAssociatedPersonProfile();
            if (profile instanceof FmcgAdmin fmcgAdmin) {
                FMCGAdminWorkArea FMCG_Admin = new FMCGAdminWorkArea(mainWorkArea, fmcgAdmin, marketDirectory);
                mainWorkArea.add("FMCG WorkArea", FMCG_Admin);
                CardLayout layout = (CardLayout) mainWorkArea.getLayout();
                layout.next(mainWorkArea);
            }
        }

    }//GEN-LAST:event_btnLoginLoginButtonActionPerformed


