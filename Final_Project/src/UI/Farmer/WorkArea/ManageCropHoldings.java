    private void btnViewContractDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewContractDetailsActionPerformed
        // TODO add your handling code here:
        if (cmbMyContracts.getSelectedItem() != null) {
            for(Contract contractTemp: farmerContracts){
                if (cmbMyContracts.getSelectedItem().toString().equals(contractTemp.getMarket().getMarketName())){
                    txtCropName.setText(contractTemp.getSelectedItem().getCrop().getCropName());
                    txtGrade.setText(contractTemp.getSelectedItem().getCrop().getGrade());
                    txtQuantity.setText(String.valueOf(contractTemp.getSelectedItem().getQuantity()));
                }
            }
        }
    }//GEN-LAST:event_btnViewContractDetailsActionPerformed

    private void btnGrowandSendQAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrowandSendQAActionPerformed
        // TODO add your handling code here:
        if (cmbMyContracts.getSelectedItem() != null) {
            for(Contract contractTemp: farmerContracts){
                if (cmbMyContracts.getSelectedItem().toString().equals(contractTemp.getMarket().getMarketName())){
                    populateHoldingsTable(contractTemp);
                }
            }
        }
    }//GEN-LAST:event_btnGrowandSendQAActionPerformed

    private void populateFarmerContractCombo() {
        cmbMyContracts.removeAllItems();
        for(Contract contract : farmerContracts){
            cmbMyContracts.addItem(contract.getMarket().getMarketName());
        }
    }

    private void populateHoldingsTable(Contract cont) {
        try {
            int rc = tblHoldings.getRowCount();
            int i;
            for (i = rc - 1; i >= 0; i--) {
                ((DefaultTableModel) tblHoldings.getModel()).removeRow(i);
            }
            
                Object[] row = new Object[4];
                row[0] = cont.getSelectedItem().getCrop().getCropName();
                row[1] = cont.getSelectedItem().getCrop().getGrade();
                row[2] = cont.getSelectedItem().getQuantity();
                row[3] = cont.isQualityAssuranceApproval();
                ((DefaultTableModel) tblHoldings.getModel()).addRow(row);
           
        } catch (Exception e) {
            System.out.println("Exception in Populating Farmer Holdings: " + e.getMessage());
        }
    }


